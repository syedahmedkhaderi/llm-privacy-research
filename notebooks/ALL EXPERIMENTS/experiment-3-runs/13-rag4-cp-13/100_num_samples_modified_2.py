class SimpleRetriever:
    """
    Basic retriever using sentence embeddings and FAISS for similarity search.
    """
    def __init__(self, embedding_model='all-MiniLM-L6-v2'):
        """
        Initialize the retriever with an embedding model.

        Args:
            embedding_model: Name of the sentence-transformers model to use
        """
        print(f"Loading embedding model: {embedding_model}")
        try:
            self.encoder = SentenceTransformer(embedding_model)
            self.index = None
            self.documents = []
            self.embedding_dim = self.encoder.get_sentence_embedding_dimension()
            print(f"‚úì Encoder loaded successfully (dimension: {self.embedding_dim})")
        except Exception as e:
            print(f"‚úó Error loading encoder: {e}")
            raise

    def build_index(self, documents: List[str]):
        """
        Build FAISS index from document corpus.

        Args:
            documents: List of text documents to index
        """
        if not documents:
            raise ValueError("Cannot build index with empty document list")

        print(f"Encoding {len(documents)} documents...")
        self.documents = documents

        try:
            # Generate embeddings for all documents
            embeddings = self.encoder.encode(
                documents,
                show_progress_bar=True,
                convert_to_numpy=True,
                batch_size=32  # Add batch processing
            )

            # Ensure embeddings are float32
            embeddings = embeddings.astype('float32')

            # Create FAISS index
            self.index = faiss.IndexFlatIP(self.embedding_dim)

            # Normalize embeddings for cosine similarity
            faiss.normalize_L2(embeddings)

            # Add to index
            self.index.add(embeddings)
            print(f"‚úì Index built with {self.index.ntotal} documents")

        except Exception as e:
            print(f"‚úó Error building index: {e}")
            raise

    def retrieve(self, query: str, top_k: int = 3) -> List[Tuple[str, float]]:
        """
        Retrieve top-k most relevant documents for a query.

        Args:
            query: Query text
            top_k: Number of documents to retrieve

        Returns:
            List of (document, score) tuples
        """
        if self.index is None:
            raise ValueError("Index not built. Call build_index first.")

        if not query or not query.strip():
            print("Warning: Empty query, returning empty results")
            return []

        try:
            # Encode query
            query_embedding = self.encoder.encode(
                [query],
                convert_to_numpy=True
            ).astype('float32')

            faiss.normalize_L2(query_embedding)

            # Ensure top_k doesn't exceed number of documents
            top_k = min(top_k, len(self.documents))

            # Search
            scores, indices = self.index.search(query_embedding, top_k)

            # Return documents with scores
            results = []
            for idx, score in zip(indices[0], scores[0]):
                if 0 <= idx < len(self.documents):
                    results.append((self.documents[idx], float(score)))

            return results

        except Exception as e:
            print(f"‚úó Error during retrieval: {e}")
            return []

